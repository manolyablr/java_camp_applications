public class DortIslem {
	
	public int topla(int number1, int number2) {
        int toplam = number1 + number2;
        return toplam;
	}

	public int cıkar(int number1, int number2) {
        int cıkar = number1 - number2;
        return cıkar;
	}
	
	public int bol(int number1, int number2) {
        int bol = number1 / number2;
        return bol;
	}
	
	public int carp(int number1, int number2) {
        int carp = number1 * number2;
        return carp;
	}
}
